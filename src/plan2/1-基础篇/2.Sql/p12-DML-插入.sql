-- 添加数据
/*
1. 给指定字段添加数据
insert into 表名(字段名1，字段名2...) values(值1，值2...);

示例：
insert into employee(id, name, age) values(1, 'shics', 23);


2. 给全部字段添加数据
insert into 表名 values(值1，值2...);

示例:
insert into employee values(1, 'shics', 23);

3. 批量添加数据
insert into 表名 (字段名1，字段名2...) values(值1，值2...),(值1，值2...);
*/